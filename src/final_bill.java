    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        String pId = txt_product_id.getText();
        String pName = txt_product_name.getText();
        String rate = txt_rate.getText();
        String description = txt_description.getText();
        
        
        try
        {
           Connection conn = connectiondb.getCon();
           Statement stat = conn.createStatement();
           stat.executeUpdate("insert into product values('"+pId+"','"+pName+"','"+rate+"','"+description+"')");
           JOptionPane.showMessageDialog(null, "Successfully Saved!");
           setVisible(false);
           new new_product().setVisible(true);
           
           
        }
        catch(HeadlessException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

