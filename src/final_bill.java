    private void txt_product_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_product_idActionPerformed
        // TODO add your handling code here:
        String pId=txt_product_id.getText();
        try
        {
            Connection con=connectiondb.getCon();
            Statement stat=con.createStatement();
            ResultSet re=stat.executeQuery("select *from product where pId='"+pId+"'");
            if(re.next())
            {
                txt_product_name.setText(re.getString(2));
                txt_rate.setText(re.getString(3));
                txt_qty.setText("1");
                txt_description.setText(re.getString(4));
            }
            else
            {
                txt_product_name.setText("");
                txt_rate.setText("");
                txt_qty.setText("");
                txt_description.setText("");
            }
        }
        catch(Exception e)
        {
            
        }
    }//GEN-LAST:event_txt_product_idActionPerformed

    private void btn_add_productActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_productActionPerformed
        // TODO add your handling code here:
        int price=Integer.parseInt(txt_rate.getText());
        int quantity=Integer.parseInt(txt_qty.getText());
        int total=price*quantity;
        DefaultTableModel mod=(DefaultTableModel)tbl_total.getModel();
        mod.addRow(new Object[]{txt_product_name.getText(),txt_description.getText(),price,quantity,total});
        finalTotal=finalTotal+total;
        String al1=String.valueOf(finalTotal);
        txt_total.setText(al1);
    }//GEN-LAST:event_btn_add_productActionPerformed

